    private void searchSidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchSidActionPerformed
           try{
            ResultSet rs =DBUtil.DatabaseConnection.search("select *from exam_results where exa_results_id = '"+searchSid.getText()+"'");
            if(rs.first()){
                examresultsId.setText(rs.getString("exa_results_id"));
                examId.setText(rs.getString("exa_id"));
                studentId.setText(rs.getString("student_id"));
                mark.setText(rs.getString("mark"));
                studentName.setText(rs.getString("student_name"));
                 subject.setText(rs.getString("subject"));
                 grade.setText(rs.getString("grade"));

            }

        }catch(Exception e){
            e.printStackTrace();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_searchSidActionPerformed

