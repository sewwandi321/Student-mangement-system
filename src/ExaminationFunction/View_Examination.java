    private void showsExamination(){
        try{
                DefaultTableModel defaulttablemodel = (DefaultTableModel)(examView.getModel());
                ResultSet rs = DBUtil.DatabaseConnection.search("select * from exam");
                while(rs.next()){
                    defaulttablemodel.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
                }   
        
        }catch(Exception e){
            e.printStackTrace();
        
        }
    
    }

