    public void TableAllData() {

        try {
            ResultSet rs =DBUtil.DatabaseConnection.search("SELECT helper_attendance.* FROM helper_attendance ");
            while (rs.next()) {
                String id = rs.getString("helper_atten_id");
                String helper_Id = rs.getString("helper_id");
                String helper_name = rs.getString("helper_name");
                String date = rs.getString("Date");

                int i = jTable4.getRowCount();
                Object[] row = {
                    id,
                    helper_Id,
                    helper_name,
                    date};

                DefaultTableModel model = (DefaultTableModel) jTable4.getModel();

                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

