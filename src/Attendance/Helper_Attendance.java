    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        System.out.println(dtf.format(now));

        String firstName = "";

        try {

            ResultSet rs = DBUtil.DatabaseConnection.search("SELECT helper.name from helper left join helper_attendance"
                    + " on helper.idhelper = helper_attendance.helper_id  where helper.idhelper='" + helper_id.getText() + "'");

            while (rs.next()) {
                firstName = rs.getString("name");
            }

           DBUtil.DatabaseConnection.iud("insert into helper_attendance(helper_id,helper_name,Date) values('" + helper_id.getText() + "','" + firstName.toString() + "','" + dtf.format(now) + "')");
            JOptionPane.showMessageDialog(null, "Added");

            DefaultTableModel model = (DefaultTableModel) jTable4.getModel();
            model.setRowCount(0);

            TableAllData();
            helper_id.setText(null);
            help_Date.setText(null);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

