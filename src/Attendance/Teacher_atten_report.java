    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            String ff = sdf.format(tfrom.getDate());
            String tt = sdf.format(tto.getDate());

            DefaultTableModel dm = (DefaultTableModel) jTable4.getModel();
            dm.getDataVector().removeAllElements();
            dm.fireTableDataChanged();

            ResultSet res;

            if (Subject.getSelectedItem().equals("Select Subject") && Grade.getSelectedItem().equals("Select grade") && tid.getText().equals("")) {

                res = DBUtil.DatabaseConnection.search("SELECT * FROM `teacher_attendance` WHERE    Date BETWEEN"
                        + ""
                        + " '" + ff + "' AND '" + tt + "'");

                JOptionPane.showMessageDialog(null, "only date");

            } else if (Subject.getSelectedItem().equals("Select Subject") && Grade.getSelectedItem().equals("Select grade")) {

                res = DBUtil.DatabaseConnection.search("SELECT * FROM `teacher_attendance` WHERE   (teacher_id LIKE '" + tid.getText() + "') AND Date BETWEEN"
                        + ""
                        + " '" + ff + "' AND '" + tt + "'");

                JOptionPane.showMessageDialog(null, "Only Teacher ID");

            } else {

                if (Subject.getSelectedItem().equals("")) {

                    res = DBUtil.DatabaseConnection.search("SELECT * FROM `teacher_attendance` WHERE   (Grade LIKE '" + Grade.getSelectedItem() + "' ) AND Date BETWEEN"
                            + ""
                            + " '" + ff + "' AND '" + tt + "'");
                } else if (Grade.getSelectedItem().equals("")) {
                    res = DBUtil.DatabaseConnection.search("SELECT * FROM `teacher_attendance` WHERE   ( subject LIKE '" + Subject.getSelectedItem() + "') AND Date BETWEEN"
                            + ""
                            + " '" + ff + "' AND '" + tt + "'");

                } else {

                    res = DBUtil.DatabaseConnection.search("SELECT * FROM `teacher_attendance` WHERE   (Grade LIKE '" + Grade.getSelectedItem() + "' AND subject LIKE '" + Subject.getSelectedItem() + "') AND Date BETWEEN"
                            + ""
                            + " '" + ff + "' AND '" + tt + "'");

                }

                JOptionPane.showMessageDialog(null, "subject and grade");

            }

            while (res.next()) {

                int i = jTable4.getRowCount();
                Object[] row = {
                    res.getString("teacher_id"),
                    res.getString("teacher_Name"),
                    res.getString("Date")
                };

                DefaultTableModel model = (DefaultTableModel) jTable4.getModel();

                model.addRow(row);

                System.out.println(res.getString("Subject") + "###################____" + res.getString("Grade"));
            }

        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton2ActionPerformed

