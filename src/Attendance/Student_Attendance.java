    public void TableAllData() {

        try {

            ResultSet rs = DBUtil.DatabaseConnection.search("SELECT student_attendance.* FROM student_attendance ");
            while (rs.next()) {
                String id = rs.getString("attendence_id");
                String stId = rs.getString("stdid");
                String student_name = rs.getString("Std_Name");
                String garde = rs.getString("Grade");
                String subject = rs.getString("Subject");

                String date = rs.getString("Date");

                int i = jTable1.getRowCount();
                Object[] row = {
                    id,
                    stId,
                    student_name,
                    garde,
                    subject,
                    date};

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

//         model.setRowCount(0);
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

