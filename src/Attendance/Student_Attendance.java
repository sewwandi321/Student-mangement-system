    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        System.out.println(dtf.format(now));

        String firstName = "";

        try {

            ResultSet rs = DBUtil.DatabaseConnection.search("SELECT student.* FROM student_attendance LEFT JOIN student"
                    + " ON student_attendance.stdid = student.idstudent "
                    + " where student.idstudent= '" + id.getText() + "'");
            while (rs.next()) {
                firstName = rs.getString("name");

            }

            DBUtil.DatabaseConnection.iud("insert into student_attendance(Subject,Stdid,Grade,Date,Std_Name) values('" + Subject.getSelectedItem() + "','" + id.getText() + "','" + Grade.getSelectedItem() + "','" + dtf.format(now) + "' ,'" + firstName.toString() + "' )");
            JOptionPane.showMessageDialog(null, "Added");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            TableAllData();

            id.setText(null);
            Date.setText(null);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

