    public void TableAllData() {

        try {
            ResultSet rs = DBUtil.DatabaseConnection.search("SELECT operator_attendance.* FROM operator_attendance ");
            while (rs.next()) {
                String id = rs.getString("operator_atten_id");
                String operator_Id = rs.getString("operator_id");
                String operator_name = rs.getString("operator_name");
                String date = rs.getString("Date");

                int i = jTable3.getRowCount();
                Object[] row = {
                    id,
                    operator_Id,
                    operator_name,
                    date};

                DefaultTableModel model = (DefaultTableModel) jTable3.getModel();

                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

