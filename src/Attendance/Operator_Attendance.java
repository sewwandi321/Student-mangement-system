    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        System.out.println(dtf.format(now));

        String firstName = "";

        try {

            ResultSet rs = DBUtil.DatabaseConnection.search("SELECT operator.name from operator left join operator_attendance"
                    + "  on operator.idoperator  = operator_attendance.operator_id where operator.idoperator='" + operator_id.getText() + "'");

            while (rs.next()) {
                firstName = rs.getString("name");

            }

            DBUtil.DatabaseConnection.iud("insert into operator_attendance(operator_id,operator_name,Date) values('" + operator_id.getText() + "','" + firstName.toString() + "','" + dtf.format(now) + "')");
            JOptionPane.showMessageDialog(null, "Added");

            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
            model.setRowCount(0);

            TableAllData();
            operator_id.setText(null);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

